/**
 * op_add - returns sum
 *
 * @a: first number
 * @b: second number
 *
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - returns difference
 *
 * @a: first number
 * @b: second number
 *
 * Return: sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - returns product
 *
 * @a: first number
 * @b: second number
 *
 * Return: mul
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - returns division
 *
 * @a: first number
 * @b: second number
 *
 * Return: div
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns remainder
 *
 * @a: first number
 * @b: second number
 *
 * Return: modulo
 */
int op_mod(int a, int b)
{
	return (a % b);
}
